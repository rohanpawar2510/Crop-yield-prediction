"""
predict_with_model.py — CropPredictor: load and use the trained crop classifier.

Provides a high-level CropPredictor class that:
  - Loads the trained RandomForestClassifier and preprocessing artifacts.
  - Scales input features with the fitted StandardScaler (if available).
  - Returns the top-3 crop predictions with confidence scores.
  - Exposes the model's test accuracy from saved metadata.

**Model loading priority**:
  1. ``ml_models/models/crop_classifier.pkl`` — trained with StandardScaler
     (generated by running prepare_dataset.py then train_model.py).
  2. ``backend/models/crop_model.pkl`` — legacy model trained without scaling.
     Used as a fallback so the API works immediately after cloning.

Usage:
    from ml_models.predict_with_model import CropPredictor

    predictor = CropPredictor()
    result = predictor.predict(N=90, P=42, K=43, temperature=21,
                               humidity=82, ph=6.5, rainfall=202)
    print(result)
"""

from __future__ import annotations

import json
import os
from typing import List, Optional

import joblib
import numpy as np

# ─── Paths ───────────────────────────────────────────────────────────────────

_SCRIPT_DIR = os.path.dirname(os.path.abspath(__file__))
_BACKEND_DIR = os.path.dirname(_SCRIPT_DIR)

_ML_MODELS_DIR = os.path.join(_SCRIPT_DIR, "models")
_PROCESSED_DIR = os.path.join(_SCRIPT_DIR, "processed")

# Legacy backend/models/ directory (fallback when ml_models/models/ is absent)
_LEGACY_MODELS_DIR = os.path.join(_BACKEND_DIR, "models")
_LEGACY_MODEL_PATH = os.path.join(_LEGACY_MODELS_DIR, "crop_model.pkl")
_LEGACY_ENCODER_PATH = os.path.join(_LEGACY_MODELS_DIR, "label_encoder.pkl")

FEATURE_NAMES = ["N", "P", "K", "temperature", "humidity", "ph", "rainfall"]


class CropPredictor:
    """Wraps the trained crop classifier for inference.

    Attributes:
        model_accuracy (float): Test accuracy from training metadata (0–100).
        crop_classes (list[str]): Ordered list of crop class names.
    """

    def __init__(self) -> None:
        self._model = None
        self._scaler = None
        self._label_encoder = None
        self._metadata: dict = {}
        self._loaded: bool = False

    # ── Lazy loading ──────────────────────────────────────────────────────

    def _load(self) -> None:
        if self._loaded:
            return

        # ── Try ml_models/models/ first (trained with StandardScaler) ──
        ml_model_path = os.path.join(_ML_MODELS_DIR, "crop_classifier.pkl")
        ml_scaler_path = os.path.join(_PROCESSED_DIR, "scaler.pkl")
        ml_encoder_path = os.path.join(_PROCESSED_DIR, "label_encoder.pkl")
        ml_meta_path = os.path.join(_ML_MODELS_DIR, "metadata.json")

        if os.path.exists(ml_model_path) and os.path.exists(ml_scaler_path):
            self._model = joblib.load(ml_model_path)
            self._scaler = joblib.load(ml_scaler_path)
            self._label_encoder = joblib.load(ml_encoder_path)
            if os.path.exists(ml_meta_path):
                with open(ml_meta_path) as fh:
                    self._metadata = json.load(fh)
            self._loaded = True
            return

        # ── Fall back to legacy backend/models/ (trained without scaler) ──
        if os.path.exists(_LEGACY_MODEL_PATH) and os.path.exists(_LEGACY_ENCODER_PATH):
            self._model = joblib.load(_LEGACY_MODEL_PATH)
            self._scaler = None  # legacy model was trained without scaler
            self._label_encoder = joblib.load(_LEGACY_ENCODER_PATH)
            self._loaded = True
            return

        raise FileNotFoundError(
            "No trained crop model found. Either:\n"
            f"  • Run ml_models/prepare_dataset.py then ml_models/train_model.py, or\n"
            f"  • Ensure {_LEGACY_MODEL_PATH!r} exists."
        )

    # ── Public API ────────────────────────────────────────────────────────

    @property
    def model_accuracy(self) -> float:
        """Test accuracy in percent (e.g. 98.64). Returns 0.0 if unknown."""
        self._load()
        return float(self._metadata.get("test_accuracy", 0.0))

    @property
    def crop_classes(self) -> List[str]:
        """All crop class names supported by the model."""
        self._load()
        return list(self._label_encoder.classes_)

    def predict(
        self,
        N: float,
        P: float,
        K: float,
        temperature: float,
        humidity: float,
        ph: float,
        rainfall: float,
        top_n: int = 3,
    ) -> dict:
        """Predict the best crop(s) for the given soil / climate parameters.

        Args:
            N: Nitrogen content in kg/ha.
            P: Phosphorus content in kg/ha.
            K: Potassium content in kg/ha.
            temperature: Air temperature in °C.
            humidity: Relative humidity in %.
            ph: Soil pH.
            rainfall: Rainfall in mm.
            top_n: Number of top predictions to return (default 3).

        Returns:
            dict with keys:
              - ``crop``            : str — top predicted crop.
              - ``confidence``      : float — probability score (0–100).
              - ``top_predictions`` : list of {crop, confidence} dicts.
              - ``model_accuracy``  : float — model test accuracy in % (or 0 if unknown).
        """
        self._load()

        features = np.array([[N, P, K, temperature, humidity, ph, rainfall]], dtype=float)

        if self._scaler is not None:
            features = self._scaler.transform(features)

        proba = self._model.predict_proba(features)[0]
        top_indices = np.argsort(proba)[::-1][:top_n]

        top_predictions = [
            {
                "crop": str(self._label_encoder.inverse_transform([i])[0]).title(),
                "confidence": round(float(proba[i]) * 100, 2),
            }
            for i in top_indices
        ]

        best = top_predictions[0]
        return {
            "crop": best["crop"],
            "confidence": best["confidence"],
            "top_predictions": top_predictions,
            "model_accuracy": self.model_accuracy,
        }


# ── Module-level singleton ────────────────────────────────────────────────────

_predictor: Optional[CropPredictor] = None


def get_predictor() -> CropPredictor:
    """Return the module-level CropPredictor singleton (lazy-loaded)."""
    global _predictor
    if _predictor is None:
        _predictor = CropPredictor()
    return _predictor
